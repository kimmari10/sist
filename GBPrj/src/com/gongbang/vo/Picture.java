package com.gongbang.vo;

public class Picture {
	String pic_seq;
	String bbs_seq;
	String path;
	public String getPic_seq() {
		return pic_seq;
	}
	public void setPic_seq(String pic_seq) {
		this.pic_seq = pic_seq;
	}
	public String getBbs_seq() {
		return bbs_seq;
	}
	public void setBbs_seq(String bbs_seq) {
		this.bbs_seq = bbs_seq;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}

}
