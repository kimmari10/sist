package com.gongbang.vo;

public class Comment {
	
	private String bbs_seq;
	private String bbs_writer;
	private String seq;
	private String title;
	private String writer;
	private String contents;
	private String regdate;
	private int num;
	
	public String getBbs_seq() {
		return bbs_seq;
	}
	public void setBbs_seq(String bbs_seq) {
		this.bbs_seq = bbs_seq;
	}
	public String getBbs_writer() {
		return bbs_writer;
	}
	public void setBbs_writer(String bbs_writer) {
		this.bbs_writer = bbs_writer;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
